let n=parseInt(prompt('enter a no.'))
switch (n) {
    case 1:
        document.write('monday')
        break;
    case 2:
        document.write('tuesday')
        break
    case 7:
        document.write('sunday')
        break
    default:
        document.write('not a day no.')
        break;
}




